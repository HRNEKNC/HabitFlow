const STORE_LANGUAGE_KEY = "settings.lang";

const languageDetector = {
  type: "languageDetector",
  async: true,
  detect: async (callback) => {
    try {
      const savedDataJSON = await AsyncStorage.getItem(STORE_LANGUAGE_KEY);
      if (savedDataJSON) {
        callback(savedDataJSON);
      } else {
        const phoneLang = Localization.getLocales()[0].languageCode;
        if (resources[phoneLang]) {
          callback(phoneLang);
        } else {
          callback("en");
        }
      }
    } catch (error) {
      callback("en");
    }
  },
  init: () => {},
  cacheUserLanguage: async (lng) => {
    try {
      await AsyncStorage.setItem(STORE_LANGUAGE_KEY, lng);
    } catch (error) {}
  },
};

i18n
  .use(languageDetector)
  .use(initReactI18next)
  .init({
    compatibilityJSON: "v3",
    resources,
    fallbackLng: "en",
    interpolation: { escapeValue: false },
  });

export default i18n;
